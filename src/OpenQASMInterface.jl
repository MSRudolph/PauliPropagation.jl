function _translate_gate(::Val{:cu}, qubits, cargs)
    a, b = qubits
    if length(qubits) != 2
        error("cu gate must act on exactly two qubits, got $(length(qubits)) qubits")
    end
    if length(cargs) != 4
        error("cu gate requires exactly 4 parameters (theta, phi, lambda, gamma), got $(length(cargs))")
    end

    theta  = _parse_angle_token(cargs[1])
    phi    = _parse_angle_token(cargs[2])
    lambda = _parse_angle_token(cargs[3])
    gamma  = _parse_angle_token(cargs[4])

    # Helper to build a single-qubit U3 matrix (same convention as the u3 translation).
    u3_matrix(theta, phi, lambda) = begin
        cos_half = cos(theta / 2)
        sin_half = sin(theta / 2)
        exp_i_lambda = exp(1.0im * lambda)
        exp_i_phi = exp(1.0im * phi)
        exp_i_phi_lambda = exp(1.0im * (phi + lambda))
        [
            cos_half                 -exp_i_lambda * sin_half;
            exp_i_phi * sin_half      exp_i_phi_lambda * cos_half
        ]
    end

    theta1, phi1, lambda1 = -theta/2, 0.0, -(phi + lambda)/2
    theta2, phi2, lambda2 =  theta/2,  phi, 0.0

    U1 = u3_matrix(theta1, phi1, lambda1)
    U2 = u3_matrix(theta2, phi2, lambda2)

    gates = Gate[
        # p(gamma) on control
        PauliRotation(:Z, a),
        # p((lambda+phi)/2) on control
        PauliRotation(:Z, a),
        # p((lambda-phi)/2) on target
        PauliRotation(:Z, b),
        CliffordGate(:CNOT, [a, b]),
        TransferMapGate(U1, [b]),
        CliffordGate(:CNOT, [a, b]),
        TransferMapGate(U2, [b]),
    ]

    thetas = [
        gamma,
        (lambda + phi)/2,
        (lambda - phi)/2,
        nothing,
        nothing,
        nothing,
        nothing,
    ]

    return (gates, thetas)
end

function _translate_gate(::Val{:cu3}, qubits, cargs)
    a, b = qubits
    if length(cargs) != 3
        error("cu3 gate requires exactly 3 parameters (theta, phi, lambda), got $(length(cargs))")
    end

    theta  = _parse_angle_token(cargs[1])
    phi    = _parse_angle_token(cargs[2])
    lambda = _parse_angle_token(cargs[3])

    # Helper to build a single-qubit U3 matrix (same convention as u3 translation).
    u3_matrix(theta, phi, lambda) = begin
        cos_half = cos(theta / 2)
        sin_half = sin(theta / 2)
        exp_i_lambda = exp(1.0im * lambda)
        exp_i_phi = exp(1.0im * phi)
        exp_i_phi_lambda = exp(1.0im * (phi + lambda))
        [
            cos_half                 -exp_i_lambda * sin_half;
            exp_i_phi * sin_half      exp_i_phi_lambda * cos_half
        ]
    end

    # Parameters for the two U3 blocks on the target.
    theta1, phi1, lambda1 = -theta/2, 0.0, -(phi + lambda)/2
    theta2, phi2, lambda2 =  theta/2,  phi, 0.0

    U1 = u3_matrix(theta1, phi1, lambda1)
    U2 = u3_matrix(theta2, phi2, lambda2)

    gates = Gate[
        # u1((lambda+phi)/2) a  -> RZ on control
        PauliRotation(:Z, a),
        # u1((lambda-phi)/2) b  -> RZ on target
        PauliRotation(:Z, b),
        CliffordGate(:CNOT, [a, b]),
        TransferMapGate(U1, [b]),
        CliffordGate(:CNOT, [a, b]),
        TransferMapGate(U2, [b]),
    ]

    thetas = [
        (lambda + phi)/2,
        (lambda - phi)/2,
        nothing,
        nothing,
        nothing,
        nothing,
    ]

    return (gates, thetas)
end

