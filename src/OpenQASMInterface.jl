function _translate_gate(::Val{:p}, qubits, cargs)
    # p(lambda) is equivalent to rz(lambda) - phase gate
    lambda = _parse_angle_token(cargs[1])
    return (PauliRotation(:Z, qubits), lambda)
end

function _translate_gate(::Val{:u1}, qubits, cargs)
    # u1(lambda) is equivalent to a Z rotation RZ(lambda),
    lambda = _parse_angle_token(cargs[1])
    return (PauliRotation(:Z, qubits), lambda)
end

function _translate_gate(::Val{:sdg}, qubits, cargs)
    return (PauliRotation(:Z, qubits), -pi/2)
end

function _translate_gate(::Val{:tdg}, qubits, cargs)
    return (PauliRotation(:Z, qubits), -pi/4)
end

function _translate_gate(::Val{:rzz}, qubits, cargs)
    theta = _parse_angle_token(cargs[1])
    # If angle is pi/2, use the Clifford gate ZZpihalf
    if isapprox(theta, pi/2, atol=1e-10)
        return (CliffordGate(:ZZpihalf, qubits), nothing)
    else
        # Two-qubit ZZ rotation generated by Z ⊗ Z
        return (PauliRotation([:Z, :Z], qubits), theta)
    end
end

function _translate_gate(::Val{:rxx}, qubits, cargs)
    theta = _parse_angle_token(cargs[1])
    # Two-qubit XX rotation generated by X ⊗ X
    return (PauliRotation([:X, :X], qubits), theta)
end

function _translate_gate(::Val{:u3}, qubits, cargs)
    if length(qubits) != 1
        error("u3 gate must act on exactly one qubit, got $(length(qubits)) qubits")
    end
    
    if length(cargs) != 3
        error("u3 gate requires exactly 3 parameters (theta, phi, lambda), got $(length(cargs))")
    end
    
    theta  = _parse_angle_token(cargs[1])
    phi    = _parse_angle_token(cargs[2])
    lambda = _parse_angle_token(cargs[3])
    
    # Compute the u3 unitary matrix
    cos_half_theta = cos(theta / 2)
    sin_half_theta = sin(theta / 2)
    exp_i_lambda = exp(1.0im * lambda)
    exp_i_phi = exp(1.0im * phi)
    exp_i_phi_lambda = exp(1.0im * (phi + lambda))
    
    U = [
        cos_half_theta           -exp_i_lambda * sin_half_theta;
        exp_i_phi * sin_half_theta   exp_i_phi_lambda * cos_half_theta
    ]
    
    # Create TransferMapGate from the unitary matrix
    gate = TransferMapGate(U, qubits)
    
    return (gate, nothing)
end

_translate_gate(::Val{:u}, qubits, cargs) = _translate_gate(Val(:u3), qubits, cargs)

function _translate_gate(::Val{:u2}, qubits, cargs)
    if length(qubits) != 1
        error("u2 gate must act on exactly one qubit, got $(length(qubits)) qubits")
    end

    if length(cargs) != 2
        error("u2 gate requires exactly 2 parameters (phi, lambda), got $(length(cargs))")
    end

    theta = pi/2
    phi = _parse_angle_token(cargs[1])
    lambda = _parse_angle_token(cargs[2])

    # Compute the corresponding u3-style unitary matrix
    cos_half_theta = cos(theta / 2)
    sin_half_theta = sin(theta / 2)
    exp_i_lambda = exp(1.0im * lambda)
    exp_i_phi = exp(1.0im * phi)
    exp_i_phi_lambda = exp(1.0im * (phi + lambda))

    U = [
        cos_half_theta           -exp_i_lambda * sin_half_theta;
        exp_i_phi * sin_half_theta   exp_i_phi_lambda * cos_half_theta
    ]

    gate = TransferMapGate(U, qubits)
    return (gate, nothing)
end

# -- Composite Gates (return vector of gates) --

function _translate_gate(::Val{:sxdg}, qubits, cargs)
    # sxdg = { s a; h a; s a; } - inverse sqrt(X)
    gates = [CliffordGate(:S, qubits), CliffordGate(:H, qubits), CliffordGate(:S, qubits)]
    thetas = [nothing, nothing, nothing]
    return (gates, thetas)
end

function _translate_gate(::Val{:cy}, qubits, cargs)
    a, b = qubits
    gates  = [PauliRotation(:Z, b), CliffordGate(:CNOT, [a, b]), PauliRotation(:Z, b)]
    # First gate = S† ≃ RZ(-π/2), last gate = S ≃ RZ(π/2).
    thetas = [-pi/2,           nothing,                       pi/2]
    return (gates, thetas)
end

function _translate_gate(::Val{:ch}, qubits, cargs)
    # ch = controlled-H (complex decomposition)
    a, b = qubits
    gates = [
        CliffordGate(:H, b),
        PauliRotation(:Z, b),  # sdg
        CliffordGate(:CNOT, [a, b]),
        CliffordGate(:H, b),
        TGate(b),
        CliffordGate(:CNOT, [a, b]),
        TGate(b),
        CliffordGate(:H, b),
        CliffordGate(:S, b),
        CliffordGate(:X, b),
        CliffordGate(:S, a)
    ]
    thetas = [nothing, -pi/2, nothing, nothing, nothing, nothing, nothing, nothing, nothing, nothing, nothing]
    return (gates, thetas)
end

function _translate_gate(::Val{:ccx}, qubits, cargs)
    # ccx = Toffoli gate (complex decomposition)
    a, b, c = qubits
    gates = [
        CliffordGate(:H, c),
        CliffordGate(:CNOT, [b, c]),
        PauliRotation(:Z, c),  # tdg
        CliffordGate(:CNOT, [a, c]),
        TGate(c),
        CliffordGate(:CNOT, [b, c]),
        PauliRotation(:Z, c),  # tdg
        CliffordGate(:CNOT, [a, c]),
        TGate(b),
        TGate(c),
        CliffordGate(:H, c),
        CliffordGate(:CNOT, [a, b]),
        TGate(a),
        PauliRotation(:Z, b),  # tdg
        CliffordGate(:CNOT, [a, b])
    ]
    thetas = [nothing, nothing, -pi/4, nothing, nothing, nothing, -pi/4, nothing, nothing, nothing, nothing, nothing, nothing, -pi/4, nothing]
    return (gates, thetas)
end

function _translate_gate(::Val{:cswap}, qubits, cargs)
    # cswap = controlled-SWAP (Fredkin gate) = { cx c,b; ccx a,b,c; cx c,b; }
    a, b, c = qubits
    # First cx c,b
    gates = Gate[CliffordGate(:CNOT, [c, b])]
    thetas = Union{Nothing, Float64}[nothing]
    # Then ccx a,b,c (expands to 15 gates with 3 thetas)
    ccx_gates, ccx_thetas = _translate_gate(Val(:ccx), [a, b, c], cargs)
    append!(gates, ccx_gates)
    append!(thetas, ccx_thetas)
    # Finally cx c,b
    push!(gates, CliffordGate(:CNOT, [c, b]))
    push!(thetas, nothing)
    return (gates, thetas)
end

function _translate_gate(::Val{:crx}, qubits, cargs)
    # crx(lambda) = controlled RX rotation
    # { u1(pi/2) b; cx a,b; u3(-lambda/2,0,0) b; cx a,b; u3(lambda/2,-pi/2,0) b; }
    a, b = qubits
    lambda = _parse_angle_token(cargs[1])

    gates = Gate[
        PauliRotation(:Z, b),           # u1(pi/2) = rz(pi/2)
        CliffordGate(:CNOT, [a, b]),    # cx a,b
        PauliRotation(:Y, b),           # u3(-lambda/2,0,0) = ry(-lambda/2)
        CliffordGate(:CNOT, [a, b])     # cx a,b
    ]
    thetas = Union{Nothing, Float64}[pi/2, nothing, -lambda/2, nothing]

    # Final single-qubit gate: u3(lambda/2,-pi/2,0) on b.
    # Use the closed-form u3 matrix to build a TransferMapGate directly.
    theta_u3  = lambda / 2
    phi_u3    = -pi/2
    lambda_u3 = 0.0

    cos_half = cos(theta_u3 / 2)
    sin_half = sin(theta_u3 / 2)
    exp_i_lambda_u3 = exp(1.0im * lambda_u3)          # = 1
    exp_i_phi_u3    = exp(1.0im * phi_u3)             # = -im
    exp_i_phi_lambda_u3 = exp(1.0im * (phi_u3 + lambda_u3))

    U_u3 = [
        cos_half                 -exp_i_lambda_u3 * sin_half;
        exp_i_phi_u3 * sin_half   exp_i_phi_lambda_u3 * cos_half
    ]

    push!(gates, TransferMapGate(U_u3, [b]))
    push!(thetas, nothing)

    return (gates, thetas)
end

function _translate_gate(::Val{:cry}, qubits, cargs)
    # cry(lambda) = controlled RY rotation
