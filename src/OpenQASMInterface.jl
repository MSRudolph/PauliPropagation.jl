# Helper to parse numeric parameter tokens from the OpenQASM AST,
# supporting literals and simple unary minus expressions.
function _parse_angle_token(token)
    if hasproperty(token, :str)
        return parse(Float64, getproperty(token, :str))
    elseif token isa OpenQASM.Types.Neg
        return -_parse_angle_token(token.val)
    else
        error("Unsupported numeric expression in QASM parameter: $(typeof(token))")
    end
end

